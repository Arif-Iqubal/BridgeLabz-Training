package com.bookshelf;
//Book Node to store books details
public class BookNode {
	//BookNode instance
	String title;
	String author;
	String genre;
	
	//Parameterized constructor
	public BookNode(String title, String author, String genre) {
		super();
		this.title = title;
		this.author = author;
		this.genre = genre;
	}
	
	
	
	
	
}
