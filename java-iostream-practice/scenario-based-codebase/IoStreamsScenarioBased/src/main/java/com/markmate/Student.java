package com.markmate;

public class Student {

	private int rollNo;
	private String name;
	private int total;
	private double average;
	private String grade;

	public Student(int rollNo, String name, int total, double average, String grade) {
		this.rollNo = rollNo;
		this.name = name;
		this.total = total;
		this.average = average;
		this.grade = grade;
	}

}
